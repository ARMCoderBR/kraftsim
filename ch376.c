////////////////////////////////////////////////////////////////////////////////
void read_sec_chunk(ch376_t *p){


    //printf("\r\nread_sec_chunk(%d) chunk:%d\r\n",p->secnum,p->sec_chunk);

    p->backrbuf[0] = SECBLKSIZE;

    if ((p->secnum >= 1)&& (p->secnum<32)){
        memset(p->backrbuf+1,0,SECBLKSIZE);
    }
    else
    if (!p->secnum){

        memcpy(p->backrbuf+1,disk_mbr+(SECBLKSIZE*p->sec_chunk),SECBLKSIZE);
    }

    uint32_t offset = (p->secnum-32) * 512 + p->sec_chunk * 64;

    fseek(p->fvol,offset,SEEK_SET);
    fread(p->backrbuf+1,SECBLKSIZE,1,p->fvol);

    p->sec_chunk++;
}

////////////////////////////////////////////////////////////////////////////////
void ch376_exec(ch376_t *p){

    if (!p->wptr) return;

    switch(p->wbuf[0]){

        case CMD_GET_IC_VER:
            p->rbuf[0] = 0x44;
            p->rptr = 0;
            break;

        case CMD_CHECK_EXIST:
            if (p->wptr == 2){
                p->rbuf[0] = ~p->wbuf[1];
                p->rptr = 0;
            }
            break;

        case CMD_SET_USB_MODE:
            if (p->wptr == 2){
                p->rbuf[0] = ANSW_RET_SUCCESS;
                p->rptr = 0;
                p->nextintr = ANSW_USB_INT_CONNECT;
                p->spistatus &= ~STAT_INTR;
            }
            break;

        case CMD_DISK_MOUNT:
            if (p->wptr == 1){
                p->rbuf[0] = ANSW_RET_SUCCESS;
                p->rptr = 0;
                p->nextintr = ANSW_USB_INT_SUCCESS;
                p->spistatus &= ~STAT_INTR;
                memcpy(p->backrbuf,(void*)"\x0cKraft80 Disk",13);
            }
            break;

        case CMD_GET_STATUS:
            if (p->wptr == 1){
                p->rbuf[0] = p->nextintr;
                p->rptr = 0;
                p->spistatus |= STAT_INTR;
            }
            break;

        case CMD_RD_USB_DATA0:
            if (p->wptr == 1){
                p->rptr = p->wptr = 0;
                memcpy(p->rbuf,p->backrbuf,SPIBUFSZ);
            }
            break;

        case CMD_DISK_READ:
            if (p->wptr == 6){
                p->secnum = p->wbuf[4];
                p->secnum <<= 8;
                p->secnum |= p->wbuf[3];
                p->secnum <<= 8;
                p->secnum |= p->wbuf[2];
                p->secnum <<= 8;
                p->secnum |= p->wbuf[1];
                p->sec_chunk = 0;
                read_sec_chunk(p);
                p->nextintr = ANSW_USB_INT_DISK_READ;
                p->spistatus &= ~STAT_INTR;
            }
            break;

        case CMD_DISK_RD_GO:
            read_sec_chunk(p);
            if (p->sec_chunk < 9)
                p->nextintr = ANSW_USB_INT_DISK_READ;
            else
                p->nextintr = ANSW_USB_INT_SUCCESS;
            p->spistatus &= ~STAT_INTR;
            break;
    }
}

////////////////////////////////////////////////////////////////////////////////
ch376_t *ch376_init(void){

    ch376_t *p = malloc(sizeof(ch376_t));

    p->spistatus = STAT_INTR;

    p->fvol = fopen("../disk.vol","r");

    return p;
}

////////////////////////////////////////////////////////////////////////////////
void ch376_out(ch376_t *p, uint8_t port, uint8_t value){

    switch(port){

        case PORTSPICTL:
            p->spictl = value;
            if (!(p->spictl & CTL_CS)){
                p->rptr = p->wptr = 0;
                //printf(" CS OFF ");
            }
            else{
                //printf(" CS ON ");
            }
            break;

        case PORTSPIDATA:
            if (p->spictl & CTL_CS){
                p->wbuf[p->wptr++] = value;
                if (p->wptr == SPIBUFSZ)
                    p->wptr = 0;
                //printf(" WR:%02x ",value);
                ch376_exec(p);
            }

            break;
    }
}

////////////////////////////////////////////////////////////////////////////////
uint8_t ch376_in(ch376_t *p, uint8_t port){

    uint8_t data = 0xff;

    switch(port){

        case PORTSPISTATUS:
            return p->spistatus;

        case PORTSPIDATA:
            if (p->spictl & CTL_CS){
                data = p->rbuf[p->rptr];
                p->rptr++;
                if (p->rptr == SPIBUFSZ)
                    p->rptr = 0;

                //printf(" RD:%02x ",data);
            }
    }

    return data;
}

////////////////////////////////////////////////////////////////////////////////
void ch376_end(ch376_t *p){

    fclose(p->fvol);
    free(p);
}
